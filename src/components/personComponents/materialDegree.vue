<template>
  <div style="width:100%;float:left;margin-top:30px;margin-bottom:30px;">
    <div class="card-title">
      <font class="font" style="font-size:25px;"><span class="titleflag"></span>学历证明</font>
    </div>
    <div ref="degreeXX" class="block upload-material">
      <ul v-if="degreeFiles && degreeFiles.length" class="el-upload-list el-upload-list--picture-card">
        <li v-for="(item, $index) in degreeFiles" :key="item.uid" tabindex="0" class="el-upload-list__item is-ready">
          <img :src="item.url" alt="" class="el-upload-list__item-thumbnail" @click="previewImg(item.url)">
          <div class="el-upload-list-action">
            <span class="item-name">{{ item.name }}</span>
            <div class="edit-select-file">
              <el-upload action="#" accept=".jpg, .png" list-type="picture-card" :auto-upload="false" :show-file-list="false" :on-change="handleEditChange">
                <span class="item-handle-btn edit-item" @click="editItemImg($index)">修改</span>
              </el-upload>
              <span class="item-handle-btn delete-item" @click="deleteItem(row, $index)">删除</span>
            </div>
          </div>
        </li>
      </ul>
      <el-upload class="upload-person-material" action="#" multiple accept=".jpg, .png" list-type="picture-card" :auto-upload="false" :show-file-list="false" :on-change="handleChange" :file-list="degreeFiles">
        <el-button slot="trigger" size="cats-common">上传照片</el-button>
        <el-button v-if="showSubmitBtn > -1" style="margin: 20px 18px 0;" size="cats-common" type="success" @click="submitUpload">上传到服务器</el-button>
      </el-upload>
      <el-dialog title="查看" :visible.sync="dialogVisible">
        <img width="100%" :src="dialogImageUrl" alt="">
      </el-dialog>
    </div>
  </div>
</template>
<script>
export default {
  name: 'materialDegree', //学历证明材料
  props: {
    params: {
      type: Object,
      default: () => {
        return {
          type: 'add',
          id: ''
        }
      },
      required: true
    },
    savePic: {
      type: String,
      default: '',
      required: true
    }
  },
  data() {
    return {
      imageUrl: '',
      degreeFiles: [],
      dialogImageUrl: '',
      dialogVisible: false,
      disabled: false,
      editImgIndex: null,
      eduPics: []
    }
  },
  computed: {
    showSubmitBtn() {
      return this.degreeFiles.findIndex(item => item.status === 'ready')
    }
  },
  created() {
    this.showUploadImg()
  },
  methods: {
    // 上传照片回显
    showUploadImg() {
      if (this.savePic) {
        const imgs = this.savePic.split('###')
        imgs.forEach((item, index) => {
          this.degreeFiles.push({
            url: item,
            uid: index,
            isSave: true
          })
          this.eduPics.push(item)
        })
      }
    },
    handleChange(file, fileList) {
      this.degreeFiles = fileList
    },
    // 上传到服务器
    submitUpload() {
      if (this.degreeFiles && this.degreeFiles.length) {
        const formData = new FormData()
        this.degreeFiles.forEach(item => {
          if (item.status === 'ready') {
            formData.append('file', item.raw)
          }
        })
        this.saveMaterialNo(formData)
      }
    },
    // 点击图片弹出预览
    previewImg(url) {
      this.dialogImageUrl = url
      this.dialogVisible = true
    },
    // 删除图片
    deleteItem(row, index) {
      this.degreeFiles.splice(index, 1)
      if (row.status === 'success') {
        this.eduPics.splice(index, 1)
        this.deleteImgRefresh()
      }
    },
    // 修改图片
    editItemImg(index) {
      this.editImgIndex = index
    },

    // 修改图片重新选择图片
    handleEditChange(file, fileList) {
      fileList.splice(0, fileList.length)
      this.degreeFiles.splice(this.editImgIndex, 1, file)
      if (file.status === 'ready') {
        const formData = new FormData()
        formData.append('file', file.raw)
        this.saveMaterialNo(formData, this.editImgIndex)
      }
    },
    // 保存图片
    saveMaterialNo(formData, fileIndex) {
      const loading = this.$loading({
        lock: true,
        text: '正在上传',
        spinner: 'car-loading',
        customClass: 'loading-box',
        background: 'rgba(234,237,244, 0.8)'
      })
      let _this = this
      this.$store.dispatch('uploadMaterial', formData).then(res => {
        if (res.code === 200) {
          if (fileIndex === undefined) {
            res.data.forEach(item => {
              _this.eduPics.push(item.storagePath)
            })
            _this.degreeFiles.forEach(item => (item.status = 'success'))
          } else {
            _this.eduPics.splice(fileIndex, 1, res.data[0].storagePath)
            _this.degreeFiles[fileIndex].status = 'success'
          }
          const saveFile = {
            personId: _this.params.id,
            eduPic: _this.eduPics.join('###')
          }
          _this.$store.dispatch('personUploadMaterial', saveFile).then(
            res => {
              loading.close()
            },
            err => {
              loading.close()
              _this.catsMessage({
                type: 'error',
                message: err.msg || ''
              })
            }
          )
        }
      }),
        err => {
          loading.close()
          _this.catsMessage({
            type: 'error',
            message: err.msg || ''
          })
        }
    },
    // 删除图片后更新人员信息
    deleteImgRefresh() {
      const loading = this.$loading({
        lock: true,
        text: '正在删除',
        spinner: 'car-loading',
        customClass: 'loading-box',
        background: 'rgba(234,237,244, 0.8)'
      })
      let _this = this
      const saveFile = {
        personId: _this.params.id,
        eduPic: _this.eduPics.join('###')
      }
      this.$store.dispatch('personUploadMaterial', saveFile).then(
        res => {
          loading.close()
        },
        err => {
          loading.close()
          _this.catsMessage({
            type: 'error',
            message: err.msg || ''
          })
        }
      )
    }
  }
}
</script>
